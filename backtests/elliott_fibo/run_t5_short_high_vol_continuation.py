def named_summary(df: pd.DataFrame) -> pd.DataFrame:
    rows = []
    for name, (fn, notes) in named_filters().items():
        rows.append(summarize_case(name, df[as_bool(fn(df))], notes))
    return pd.DataFrame(rows)


def condition_grid(df: pd.DataFrame) -> pd.DataFrame:
    symbol_modes = {
        "all": lambda d: pd.Series(True, index=d.index),
        "gbp_aud": lambda d: d["symbol"].isin(["GBPJPY", "AUDJPY"]),
        "gbp_only": lambda d: d["symbol"].eq("GBPJPY"),
    }
    trigger_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "rebreak": lambda d: d["trigger_type"].eq("rebreak"),
        "not_combo": lambda d: ~d["trigger_type"].eq("stagnation+rebreak"),
    }
    adx_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "ge25": lambda d: d["adx14"].ge(25),
        "30_40": lambda d: d["adx14"].between(30, 40),
    }
    width_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "ge7": lambda d: d["bb_width_atr"].ge(7),
        "7_10": lambda d: d["bb_width_atr"].between(7, 10),
    }
    bbpos_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "le0": lambda d: d["bb_pos"].le(0),
        "0_025": lambda d: d["bb_pos"].between(0, 0.25),
    }
    macd_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "gt0": lambda d: d["macd_hist_slope3"].gt(0),
        "gt_neg001": lambda d: d["macd_hist_slope3"].gt(-0.01),
        "neg008_neg005": lambda d: d["macd_hist_slope3"].between(-0.08, -0.05),
    }
    atr_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "pct_ge80": lambda d: d["atr_pctile_252"].ge(80),
    }
    recovery_modes = {
        "any": lambda d: pd.Series(True, index=d.index),
        "gt16": lambda d: d["signal_recovery_bars"].gt(16),
        "gt24": lambda d: d["signal_recovery_bars"].gt(24),
    }

    rows = []
    groups = [
        ("symbol", symbol_modes),
        ("trigger", trigger_modes),
        ("adx", adx_modes),
        ("bb_width", width_modes),
        ("bb_pos", bbpos_modes),
        ("macd", macd_modes),
        ("atr", atr_modes),
        ("recovery", recovery_modes),
    ]
    for choices in product(*[list(options.items()) for _, options in groups]):
        labels = dict((group_name, choice_name) for (group_name, _), (choice_name, _) in zip(groups, choices))
        mask = pd.Series(True, index=df.index)
        for (_, _), (_, fn) in zip(groups, choices):
            mask &= as_bool(fn(df))
        sample = df[mask]
        if len(sample) < 8:
            continue
        row = {**labels, **summarize_case("grid", sample)}
        rows.append(row)

    grid = pd.DataFrame(rows)
    if grid.empty:
        return grid
    grid["score"] = (
        grid["all_total_r"]
        + grid["all_avg_r"] * 5
        + np.minimum(grid["all_pf"].replace(math.inf, 99), 5)
        - grid["all_max_dd_r"] * 0.2
        - np.where(grid["oos_trades"].eq(0), 2.0, 0.0)
    )
    return grid.sort_values(["score", "all_total_r"], ascending=False).reset_index(drop=True)


def by_period_year(df: pd.DataFrame, mask: pd.Series) -> pd.DataFrame:
    sample = df[as_bool(mask)].copy()
    if sample.empty:
        return pd.DataFrame()
    rows = []
    for year, group in sample.groupby("year"):
        row = {"year": year}
        row.update(metrics(group))
        rows.append(row)
    return pd.DataFrame(rows).sort_values("year")


