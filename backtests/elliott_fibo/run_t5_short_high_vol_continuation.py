SOURCE = THIS_DIR / "results_2026_05_28" / "t5_short_mirror_validation" / "short_t5_broad_trades_2015_2026.csv"
OUT_DIR = THIS_DIR / "results_2026_05_28" / "t5_short_high_vol_continuation"
OUT_DIR.mkdir(parents=True, exist_ok=True)


def profit_factor(r: pd.Series) -> float:
    wins = float(r[r > 0].sum())
    losses = float(r[r <= 0].sum())
    if losses < 0:
        return wins / abs(losses)
    return math.inf if wins > 0 else math.nan


def max_drawdown(r: pd.Series) -> float:
    if r.empty:
        return 0.0
    curve = r.astype(float).cumsum()
    return float((curve.cummax() - curve).max())


def max_losing_streak(r: pd.Series) -> int:
    cur = 0
    best = 0
    for value in r.astype(float):
        if value <= 0:
            cur += 1
            best = max(best, cur)
        else:
            cur = 0
    return best


def metrics(df: pd.DataFrame) -> dict[str, float | int]:
    r = df["r_after_cost"].astype(float) if not df.empty else pd.Series(dtype=float)
    return {
        "trades": int(len(r)),
        "win_rate": float((r > 0).mean() * 100) if len(r) else 0.0,
        "total_r": float(r.sum()) if len(r) else 0.0,
        "avg_r": float(r.mean()) if len(r) else 0.0,
        "pf": profit_factor(r) if len(r) else math.nan,
        "max_dd_r": max_drawdown(r),
        "max_losing_streak": max_losing_streak(r),
    }


def summarize_case(name: str, df: pd.DataFrame, notes: str = "") -> dict:
    research = df[df["period"].eq("Research_2015_2024")] if not df.empty else df
    oos = df[df["period"].eq("OOS_2025_2026")] if not df.empty else df
    row = {"case": name, "notes": notes}
    row.update({f"all_{k}": v for k, v in metrics(df).items()})
    row.update({f"research_{k}": v for k, v in metrics(research).items()})
    row.update({f"oos_{k}": v for k, v in metrics(oos).items()})
    return row


FilterFn = Callable[[pd.DataFrame], pd.Series]


def as_bool(s: pd.Series) -> pd.Series:
    return s.fillna(False).astype(bool)


