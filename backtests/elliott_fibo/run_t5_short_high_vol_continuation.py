"""
High-volatility downtrend-continuation study for the short T5 universe.

This is Idea 1 from the short-side review:

- Do not mirror the long setup with low BB position + falling MACD.
- Treat inverse-V/T5 as context, then sell only when the market looks like a
  real volatility expansion / trend continuation.

The source universe is generated by run_t5_short_mirror_validation.py.
"""

