def main() -> None:
    if not SOURCE.exists():
        raise FileNotFoundError(f"Run run_t5_short_mirror_validation.py first: {SOURCE}")

    df = pd.read_csv(SOURCE, parse_dates=["signal_time", "entry_time", "exit_time"])
    named = named_summary(df)
    grid = condition_grid(df)

    recommended_mask = (
        df["trigger_type"].eq("rebreak")
        & df["adx14"].ge(25)
        & df["bb_width_atr"].between(7, 10)
        & df["bb_pos"].between(0, 0.25)
    )
    recommended = df[recommended_mask.fillna(False)].copy()
    by_year = by_period_year(df, recommended_mask)

    named.to_csv(OUT_DIR / "named_candidates.csv", index=False)
    grid.to_csv(OUT_DIR / "grid_candidates.csv", index=False)
    recommended.to_csv(OUT_DIR / "recommended_trades.csv", index=False)
    by_year.to_csv(OUT_DIR / "recommended_by_year.csv", index=False)
    write_report(df, named, grid, recommended, by_year)

    print(f"Report: {OUT_DIR / 'report_ja.md'}")
    print(named[["case", "all_trades", "all_win_rate", "all_total_r", "all_avg_r", "all_pf", "oos_trades", "oos_total_r"]].to_string(index=False))
    print("\nGrid top:")
    if not grid.empty:
        print(
            grid[
                [
                    "symbol",
                    "trigger",
                    "adx",
                    "bb_width",
                    "bb_pos",
                    "macd",
                    "atr",
                    "recovery",
                    "all_trades",
                    "all_total_r",
                    "all_avg_r",
                    "all_pf",
                    "oos_trades",
                    "oos_total_r",
                ]
            ]
            .head(20)
            .to_string(index=False)
        )


if __name__ == "__main__":
    main()
